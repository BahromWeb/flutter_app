import 'package:flutter/material.dart';
import 'package:flutter_ui_design/Live%20Streming%20App/Constants/theme_provider.dart';
import 'package:flutter_ui_design/Live%20Streming%20App/Screen/streming_app_home_screen.dart';
import 'package:provider/provider.dart';

void main() {
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  // Root widget of the app
  @override
  Widget build(BuildContext context) {
    return ChangeNotifierProvider(
      create: (_) => ThemeProvider(),
      child: Consumer<ThemeProvider>(
        builder: (context, themeProvider, child) {
          return MaterialApp(
            debugShowCheckedModeBanner: false,
            theme: themeProvider.themeData,
            home: const StremingAppHomeScreen(),
          );
        },
      ),
    );
  }
}

